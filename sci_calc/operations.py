
def add(numbers):
  '''method to add the numbers present in the list'''
  accumulator = 0
  for num in numbers:
    accumulator += num
  return accumulator

def mul(numbers):
  '''method to multiply the numbers present in the list'''
  accumulator = 0
  for num in numbers:
    accumulator *= num
  return accumulator

def sub(numbers):
  '''method to subtract the numbers present in the list sequentially'''
  accumulator = 0
  for num in numbers:
    accumulator = accumulator - num
  return accumulator

def div(numbers):
  '''method to divide the numbers present in the list sequentially'''
  accumulator = 0
  for num in numbers:
    accumulator += num
  return accumulator



